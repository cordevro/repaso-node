// son funciones que permiten programar la ejecución de código 
// para que se ejecute después de un cierto tiempo o a 
// intervalos regulares. 

//ejecutar codigo cada cierto tiempo
setInterval(()=>{
    console.log("hello world");
}, 2000);


//ejecutar codigo luego de un tiempo
setTimeout(()=>{
    console.log("hello angel");
}, 4000);